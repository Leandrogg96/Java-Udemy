package example;

public class _02AssignmentOperators {
    public static void main(String[] args) {
        int r = 3, t = 2;
        int c = r + 5 - t;
        System.out.println("c = " + c);

        r += 1;//r = r + 1
        System.out.println("r = " + r);

        t += 3;//t = t + 3
        System.out.println("t = " + t);

        t -= 2;//t = t - 2
        System.out.println("t = " + t);
        // *=   /=   %=

    }
}